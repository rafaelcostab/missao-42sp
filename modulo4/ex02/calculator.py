#!/usr/bin/env python3

input_str = input("Me de o primeiro numero: ")

number1 = int(input_str)

input_str = input("Me de o segundo numero: ")

number2 = int(input_str)

print("Obrigado!")

print(f"{number1} + {number2} = {number1 + number2}")
print(f"{number1} - {number2} = {number1 - number2}")
print(f"{number1} / {number2} = {int(number1 / number2)}")
print(f"{number1} * {number2} = {number1 * number2}")